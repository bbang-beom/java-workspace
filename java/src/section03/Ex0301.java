package section03;

public class Ex0301 {
    public static void main(String[] args) {
        // 단항 연산자(+, -)
        int num = 100;
        int resultPlus = +num;
        int resultMinus = -num;
 
        System.out.println("resultPlus\t: " + resultPlus);
        System.out.println("resultMinus\t: " + resultMinus);
        System.out.println();
        System.out.println("resultPlus\t: " + -resultPlus);
        System.out.println("resultMinus\t: " + -resultMinus);
    }
}
